class Najczestszy{
    private String word;
    private int czestosc; 

    public Najczestszy(String word, int czestosc) {
        this.word = word;
        this.czestosc = czestosc;
    }

    public String getWord() {
        return word;
    }

    public int getCzestosc() {
        return czestosc;
    }
};